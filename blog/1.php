                    	<p class="mainimg"><img src="img/movie01.jpg" alt="百日紅" class="hoverImg"/></p>
                        <p>2015年5月9日（土）に全国公開される映画『百日紅 〜Miss HOKUSAI〜』のジャパンプレミアに参加したところ、予想外に感動。<br><br>
                        日が替わっても興奮がおさまらず。<br>
                        未だに頭の中で江戸がグルグル回っております。<br><br>
                        これは映画館で観てた方がいい作品だと思いましたので、<br>
                        勝手ながら、その理由を６つほど挙げてみました。<br><br>
                        自宅に映画館並みのホームシアターがある人は別にして、<br>
                        そうでないほとんどの人の参考になれば。<br><br><br>
                        と、その前に。<br><br>
                        私と杉浦日向子作品との出会いはつい最近であります。<br>
                        最近も最近、この映画の上映が告知されたから。<br><br>
                        まず、小倉充子に「これは必読書だ！読め！」と『二つ枕』を薦められ衝撃を受けまして。その衝撃が冷めないうちに『百日紅』の上下巻を貪り読んでいたら（小倉充子が、日向子さんのほとんどの漫画を持っており工房に置いてある）、またもや小倉充子から「『百日紅』の息抜きにこれを読め！」と『とんでもねぇ野郎』を渡されました。<br><br>
                        たいそう真面目な私は、その両作品を同時進行で読んでいたところ、『とんでもねぇ野郎』も楽しくて面白くて、ちっとも息抜きにならなくて、危うくあっちの世界から戻れなくなりそうに。<br><br>
                        小倉充子ぐらいの人ならば、あっちへ行ったり、こっちへ戻って来たりと自在だろうが、私ぐらいだとそうは簡単に行ったり来たりできないのですよ…。<br><br>
                        あの人とは体力から何から構造が違うのですから、気を抜いて真似をすると危ない目に遭うのです。気をつけねば。<br><br>
                        自宅にもエッセイが沢山あるので、他の漫画作品と合わせて、順次読破予定。<br>
                        現在『百物語』に取り掛かり中。<br><br>
                        さて、本題。</p><br>
                        <p class="title">１）本編が断然いい！</p><br>
                        <p>正直、私は、この予告編を見て「観たい！」とは思わなかったのです。<br>
                        椎名林檎の主題歌が入る前の予告編を見たから余計そうだったのかも知れませんが。<br>
                        しかし、予告編を数十秒見ただけで、映画館へ行くのを諦めるのは勿体無い！</p><br><br>
                        <p class="title">２）音で泣ける！</p><br>
                        <p>（恐らく）大川に掛かる大きな橋を武士、町人、商人他大勢が行き交う音。<br>
                        そんな大勢の江戸の人々が行き交う橋の下を舟に乗って抜けて行く際の音。<br>
                        始まって間もないこの2つのシーンで、気づいたら泣いてましたよ、わたしゃ。<br><br>
                        ある登場人物が、吹き込む風に向かってグッと黙って一点を見据えるシーンなんて、<br>
                        声を出していないけど、キャストがグッと押し黙る音を収録したんじゃないかしら。<br>
                        と思えたほど。こりゃぁ、映画館でしょ。</p><br><br>
                        <p class="title">６）TOHOシネマズ日本橋で「八海山」と共に楽しむ！</p><br>
                        <p>「百日紅」のお供には八海山（可愛いひょうたんボトル 180ml）をオススメ。ｗ<br><br>
                        ジャパンプレミアでも、小倉とビールを買っていこうかと話していたところ、<br>
                        「日向子さんの映画を観るなら日本酒だよねー♪」と意気投合。<br><br>
                        そうだ、作品鑑賞状況に下記を追加。<br><br>
                        「八海山を呑みながら鑑賞」<br><br>
                        TOHOシネマズ日本橋なら、COREDO室町2の1階にある<br>
                        「八海山 千年こうじや」でお酒と肴が手に入ります。<br><br>
                        肴を買う場合は、あまり薫りの強くないものをチョイスしましょう。<br>
                        どれを選んでいいか迷ったら、店員さんに<br>
                        「映画館で食べても迷惑にならない肴はどれですか？」と尋ねましょう。<br>
                        きっと親切に教えてくれます。<br><br><br><br>
                        以上<br><br><br>
                        映画『百日紅 〜Miss HOKUSAI〜』を映画館で観た方がいい6つの理由でした。<br><br><br>
                        そう言えば、この映画の宣伝が始まった頃、着物のおはしょりが云々って話を目にしましたが、この映画を観ている間は、そんなこと一切気になりませんでしたわ。<br>
                        私自身、それを知った当初は気になっていたのでまとめをシェアしてみたりした癖にスッカリ忘れてましたもの。<br>
                        もし、それを気にするとしたら、きっと着物好き？な人ばかりでしょう。そりゃ描写は正確な方が好きですけどね。<br><br>
                        あと、杉浦日向子ファンの人が観たら、このシーン、このセリフ要らなくない？って思うところもあると思うんですが、それは原作ファンの為ではなくて、初めて杉浦日向子さんや彼女の江戸の世界を目の当たりにする人々に対して必要な歩み寄りだと思うのです。<br>
                        恐らく、それらのシーンが無くても伝わるとは思うけれど、こうした話は、きっとたくさんのしがらみがあってのことだろうし、そもそも、そんなことを考えるのが無駄なほど、原作に対する監督の愛を感じました。表現者同士の尊敬がありました。監督、本当に日向子さんの作品が好きなんだろうなぁ〜、って。<br><br>
                        北斎の勢い、エネルギーが原作よりかなり抑えられているけど、今回は娘が主役の物語だから。そして、そうしたのは、あの杉浦日向子さんの世界をより多くの人に伝えるための手法として選んだ。そんな決意というか志が見えました。<br><br>
                        ここは間が欲しいなぁ〜と思ったところは、しっかり間が取られていたし。<br>
                        音が無くていいところは、スッキリ音が無いし。<br><br>
                        いろいろ工夫をしつつも、嫌らしくならずに、どこかシュッとして、潔い雰囲気。<br><br><br>
                        とても丁寧に、大切につくられている印象。<br><br><br>
                        最後に、俳優さんが声を担当している件についてですが、私は、これも吉だと感じました。<br>
                        江戸に生きる人の声。派手でなくていい。普通の声でいい。ちょうどいい。<br>
                        始まって少し経った頃には、すっかり耳に馴染んでいました。<br><br><br>
                        兎にも角にも、映画を楽しむだけなら、そんなことは大きな問題ではないのです。<br><br><br>
                        それより何より、この映画を観て、杉浦日向子さんを知って、日向子さんの作品に触れてくれる人がたくさん現れたらいいなと思います。<br><br><br>
                        そういう理由ですから、みなさま、是非とも映画『百日紅 〜Miss HOKUSAI〜』を映画館でたっぷりとお楽しみください！<br><br><br>
                        私は、お足を払って、もう一度、映画館へ行く予定。<br><br>
                        私の住む新宿にもTOHOシネマズが出来たばかりの様ですが、八海山があるから、やっぱり日本橋かしらね。<br><br>
                        ほぼ間違いなくBlu-rayも買うことでしょう。<br>
                        楽しみ♪ 楽しみ♪<br><br>
